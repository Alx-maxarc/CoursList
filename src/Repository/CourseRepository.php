/**
 * @param User $user L'utilisateur pour lequel récupérer les listes
 * @return array
 */
public function findByListe(Liste $liste): array
{
    return $this->createQueryBuilder('c')
        ->join('c.liste', 'l') // Supposons qu'il y ait une relation many-to-many entre Liste et User
        ->where('l = :liste')
        ->setParameter('liste', $liste)
        ->getQuery()
        ->getResult();
}


